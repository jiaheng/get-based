let _snpTable = null;
let _snpTablePromise = null;

function loadSNPTable() {
  if (_snpTable) return Promise.resolve(_snpTable);
  if (!_snpTablePromise) {
    _snpTablePromise = fetch('data/snp-health.json')
      .then(r => r.json())
      .then(data => { _snpTable = data; window._snpTableCache = data; return data; })
      .catch(err => { _snpTablePromise = null; if (window.isDebugMode?.()) console.error('Failed to load SNP table:', err); throw err; });
  }
  return _snpTablePromise;
}

// Eagerly load SNP table when genetics data exists (e.g. after JSON import)
export function ensureSNPTable() { if (state.importedData?.genetics) loadSNPTable(); }

// Returns { matches: { rsid: { genotype, gene, variant, effect, note } }, source, totalLines, coverage }
export async function parseDNAFile(file) {
  const snpTable = await loadSNPTable();
  const snpIds = Object.keys(snpTable).filter(k => k.startsWith('rs'));

  // Detect format from first chunk
  const headerChunk = await file.slice(0, 1000).text();
  const format = detectDNAFile(headerChunk);
  if (!format) throw new Error('Unrecognized DNA file format');

  // Run parser in a fresh worker (prevents handler conflicts on concurrent parses)
  const worker = createWorker();
  const result = await new Promise((resolve, reject) => {
    const timeout = setTimeout(() => { worker.terminate(); reject(new Error('DNA parsing timed out')); }, 30000);
    worker.onmessage = (e) => { clearTimeout(timeout); worker.terminate(); resolve(e.data); };
    worker.onerror = (e) => { clearTimeout(timeout); worker.terminate(); reject(new Error(e.message)); };
    worker.postMessage({ file, snpIds, format });
  });

  // Enrich matches with lookup table data
  const enriched = {};
  for (const [rsid, genotype] of Object.entries(result.matches)) {
    const entry = snpTable[rsid];
    if (!entry) continue;
    const reversed = genotype.length === 2 ? genotype[1] + genotype[0] : genotype;
    const genotypeInfo = entry.genotypes[genotype] || entry.genotypes[reversed] || entry.genotypes[sortAlleles(genotype)] || null;
    enriched[rsid] = {
      genotype,
      gene: entry.gene,
      variant: entry.variant,
      category: entry.category,
      markers: entry.markers || [],
      effect: genotypeInfo ? genotypeInfo.effect : 'unknown',
      note: genotypeInfo ? genotypeInfo.note : `Genotype ${genotype} not in lookup table`,
    };
  }

  return {
    matches: enriched,
    source: formatSourceName(result.source),
    totalLines: result.totalLines,
    coverage: { found: Object.keys(enriched).length, total: snpIds.length },
  };
}

// Sort alleles alphabetically for lookup (AG = GA)
function sortAlleles(genotype) {
  if (!genotype || genotype.length !== 2) return genotype;
  const sorted = genotype.split('').sort().join('');
  return sorted;
}

function formatSourceName(format) {
  const names = { ancestry: 'AncestryDNA', '23andme': '23andMe', livingdna: 'Living DNA', csv: 'MyHeritage/FTDNA', 'illumina-gsgt': 'Illumina GenomeStudio (DNAEra)' };
  return names[format] || format;
}

// ═══════════════════════════════════════════════
// APOE HAPLOTYPE RESOLUTION
// ═══════════════════════════════════════════════

// APOE is determined by two SNPs: rs429358 and rs7412
// rs429358 T→C = ε4 allele, rs7412 C→T = ε2 allele
// APOE haplotype from unphased genotypes — genotype-combination lookup
// Raw DNA files are UNPHASED (alleles aren't assigned to chromosomes),
// so we use the diploid genotype pair, not per-allele pairing.
// ε2: rs429358=T, rs7412=T | ε3: rs429358=T, rs7412=C | ε4: rs429358=C, rs7412=C
function resolveAPOE(matches) {
  const g429 = matches.rs429358?.genotype;
  const g7412 = matches.rs7412?.genotype;
  if (!g429 || !g7412 || g429.length !== 2 || g7412.length !== 2) return null;
  const key = `${sortAlleles(g429)}|${sortAlleles(g7412)}`;
  const table = {
    'TT|CC': '\u03B53/\u03B53', 'CT|CC': '\u03B53/\u03B54', 'CC|CC': '\u03B54/\u03B54',
    'TT|CT': '\u03B52/\u03B53', 'CT|CT': '\u03B52/\u03B54', 'TT|TT': '\u03B52/\u03B52',
  };
  return table[key] || null;
}

// ═══════════════════════════════════════════════
// STORAGE
// ═══════════════════════════════════════════════

export function saveGeneticsData(profileData, parseResult) {
  // Count effects for quick display (avoids needing SNP table at render time)
  const apoe = resolveAPOE(parseResult.matches);
  const apoeRsids = apoe ? new Set(['rs429358', 'rs7412']) : new Set();
  let significant = 0, moderate = 0, normal = 0;
  for (const [rsid, data] of Object.entries(parseResult.matches)) {
    if (apoeRsids.has(rsid)) continue;
    if (data.effect === 'significant') significant++;
    else if (data.effect === 'moderate') moderate++;
    else if (data.effect === 'none') normal++;
  }
  profileData.genetics = {
    source: parseResult.source,
    importDate: new Date().toISOString().slice(0, 10),
    coverage: parseResult.coverage,
    effects: { significant, moderate, normal },
    snps: {},
  };
  for (const [rsid, data] of Object.entries(parseResult.matches)) {
    profileData.genetics.snps[rsid] = {
      genotype: data.genotype,
      gene: data.gene,
      variant: data.variant,
    };
  }
  if (apoe) {
    profileData.genetics.apoe = apoe;
  }
}

export function deleteGeneticsData(profileData) {
  delete profileData.genetics;
}

