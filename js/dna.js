// Detect DNA file by checking first few lines of text content
export function detectDNAFile(text) {
  const first = text.slice(0, 1500);
  if (first.startsWith('#AncestryDNA')) return 'ancestry';
  if (first.includes('generated by 23andMe')) {
    // 23andMe Mito/Y files have the same header (~900 bytes) — need 1500 bytes to see data lines
    // Filter out truncated trailing line from the byte slice
    const dataLines = first.split(/\r?\n/).filter(l => l.trim() && !l.startsWith('#') && (l.match(/\t/g) || []).length >= 3);
    if (dataLines.length > 0 && dataLines.every(l => /^\S+\tMT\t/.test(l))) return '23andme-mito';
    if (dataLines.length > 0 && dataLines.every(l => /^\S+\tY\t/.test(l))) return '23andme-y';
    return '23andme';
  }
  if (first.includes('# Living DNA customer genotype data download file version')) return 'livingdna';
  // Illumina GenomeStudio export (DNAEra and other clinical labs running Illumina arrays):
  // BOM-prefixed [Header] block with GSGT Version, then [Data] block with comma-separated
  // "Sample Name,SNP Name,Chr,Position,Allele1 - Plus,Allele2 - Plus" rows.
  if (first.includes('GSGT Version,')) return 'illumina-gsgt';
  // CSV formats — no comment lines, start with header
  const firstLine = first.split(/\r?\n/)[0].trim();
  if (/^RSID,CHROMOSOME,POSITION,RESULT$/i.test(firstLine)) return 'csv'; // MyHeritage or FTDNA
  if (/^"?RSID"?,/i.test(firstLine)) return 'csv'; // quoted variant
  // Tab-separated: rsid\tchromosome\tposition\tgenotype (23andMe without header, or stripped)
  if (/^rsid\tchromosome\tposition\tgenotype/i.test(firstLine)) return '23andme';
  // Headerless: lines starting with rs followed by tab-separated fields (rsid\tchr\tpos\tAA)
  const dataLines = first.split(/\r?\n/).filter(l => l.trim() && !l.startsWith('#'));
  if (dataLines.length > 0 && /^rs\d+\t\d+\t\d+\t[ACGT\-]{1,2}$/i.test(dataLines[0].trim())) return '23andme';
  // mtDNA CSV: short files with position+allele lines like "263G", "10398G"
  if (dataLines.length > 0 && dataLines.length < 200 && dataLines.filter(l => /^\d+[ACGT]$/i.test(l.trim())).length >= dataLines.length * 0.8) return 'mtdna';
  return null;
}

// Check if a dropped/selected file looks like a DNA raw data file (by name only — sync check)
export function isDNAFile(file) {
  if (!file) return false;
  const name = file.name.toLowerCase();
  // Common DNA file patterns
  if (name.includes('ancestrydna')) return true;
  if (name.includes('23andme')) return true;
  if (name.includes('myheritage') && name.includes('dna')) return true;
  if (name.includes('family_finder')) return true;
  if (name.includes('livingdna') || name.includes('living_dna')) return true;
  if (name.includes('genome') || name.includes('genotype') || name.includes('raw_dna') || name.includes('rawdna')) return true;
  if (name.includes('mtdna') || name.includes('mt-dna') || name.includes('mt_dna')) return true;
  if (name.includes('dnaera')) return true;
  return false;
}

// Content-based DNA detection — reads first 1500 bytes (async, needs to see past 23andMe header)
export async function isDNAFileByContent(file) {
  try {
    const header = await file.slice(0, 1500).text();
    return detectDNAFile(header) !== null;
  } catch { return false; }
}

// ═══════════════════════════════════════════════
// WEB WORKER PARSER (inline blob)
// ═══════════════════════════════════════════════

const WORKER_CODE = `
// DNA parser worker — receives { file, snpIds, format }
// Posts back { matches, source, totalLines, format }

self.onmessage = async function(e) {
  const { file, snpIds, format } = e.data;
  const text = await file.text();
  const lines = text.split(/\\r?\\n/);
  const lookupSet = new Set(snpIds);
  const matches = {};
  let totalData = 0;
  let detectedFormat = format;
  // Illumina GSGT files have a [Header] block that must be skipped — flip
  // this true once we hit [Data] and discard the column-header line that
  // follows it.
  let illuminaInData = false;

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    if (!line || line.startsWith('#')) continue;

    // Skip header lines
    const upper = line.toUpperCase();
    if (upper.startsWith('RSID') || upper.startsWith('"RSID')) continue;

    let rsid, genotype;

    if (detectedFormat === 'illumina-gsgt') {
      // Walk the [Header] block until [Data], then skip the column-header
      // line ("Sample Name,SNP Name,Chr,Position,Allele1 - Plus,Allele2 - Plus")
      // before any rows are eligible.
      const trimmed = line.trim();
      if (!illuminaInData) {
        if (/^\[Data\]/i.test(trimmed)) illuminaInData = true;
        continue;
      }
      if (/^Sample Name/i.test(trimmed)) continue;
      const parts = trimmed.split(',');
      if (parts.length < 6) continue;
      rsid = parts[1].trim();           // SNP Name
      // Illumina probes wrap rsids many ways: seq-rs1234, GSA-rs1234,
      // ilmnseq_rs1234_ilmnTOP_5AT, BOT-rs1234, dup-seq-rs1234, etc.
      // Extract the bare rsid so a wrapped probe still hits the lookup.
      const m = rsid.match(/(rs\\d+)/);
      if (m) rsid = m[1];
      const a1 = parts[4].trim();        // Allele1 - Plus
      const a2 = parts[5].trim();        // Allele2 - Plus
      if (a1 === '-' || a2 === '-' || a1 === '0' || a2 === '0') { totalData++; continue; }
      genotype = a1 + a2;
    } else if (detectedFormat === 'ancestry') {
      // Tab-separated: rsid, chromosome, position, allele1, allele2
      const parts = line.split('\\t');
      if (parts.length < 5) continue;
      rsid = parts[0].trim();
      const a1 = parts[3].trim();
      const a2 = parts[4].trim();
      if (a1 === '0' || a2 === '0' || a1 === '-' || a2 === '-') { totalData++; continue; }
      genotype = a1 + a2;
    } else if (detectedFormat === '23andme' || detectedFormat === 'livingdna') {
      // Tab-separated: rsid, chromosome, position, genotype
      const parts = line.split('\\t');
      if (parts.length < 4) continue;
      rsid = parts[0].trim();
      genotype = parts[3].trim();
      if (genotype === '--' || genotype === '00') { totalData++; continue; }
    } else if (detectedFormat === 'csv') {
      // Comma-separated: RSID, CHROMOSOME, POSITION, RESULT (possibly quoted)
      const parts = line.split(',').map(s => s.replace(/^"|"$/g, '').trim());
      if (parts.length < 4) continue;
      rsid = parts[0];
      genotype = parts[3];
      if (genotype === '--' || genotype === '00' || !genotype) { totalData++; continue; }
    } else {
      continue;
    }

    // Skip internal IDs (23andMe uses i-prefixed IDs for some markers)
    if (!rsid.startsWith('rs')) { totalData++; continue; }

    totalData++;

    if (lookupSet.has(rsid) && !matches[rsid]) {
      // First non-missing call wins. Illumina chips often have multiple
      // probes for the same SNP (seq-rsX + seq-rsX.1 + seq-rsX.2). They
      // usually agree, but if a later probe failed, keeping the first
      // valid call is safer than overwriting it.
      matches[rsid] = genotype;
    }
  }

  self.postMessage({ matches, source: detectedFormat, totalLines: totalData, format: detectedFormat });
};
`;

let _workerBlobUrl = null;

function createWorker() {
  if (!_workerBlobUrl) {
    const blob = new Blob([WORKER_CODE], { type: 'application/javascript' });
    _workerBlobUrl = URL.createObjectURL(blob);
  }
  return new Worker(_workerBlobUrl);
}

