// DASHBOARD SECTION
// ═══════════════════════════════════════════════

export function renderGeneticsSection() {
  const genetics = state.importedData.genetics;
  const hasSnps = genetics && genetics.snps && Object.keys(genetics.snps).length > 0;
  const hasMtdna = genetics && genetics.mtdna;
  if (!hasSnps && !hasMtdna) return '';
  if (hasSnps && !_snpTable) {
    loadSNPTable().then(() => { if (window.navigate) window.navigate('dashboard'); });
    return '';
  }
  const snpTable = _snpTable;
  const snpCount = hasSnps ? Object.keys(genetics.snps).length : 0;
  const apoe = genetics.apoe;
  const collapsed = localStorage.getItem('labcharts-genetics-collapsed') === '1';

  // Group findings by category, skip "none"
  const byCat = {};
  const apoeRsids = new Set(['rs429358', 'rs7412']);
  for (const [rsid, stored] of Object.entries(genetics.snps || {})) {
    if (apoe && apoeRsids.has(rsid)) continue;
    const entry = snpTable?.[rsid];
    if (!entry) continue;
    const reversed = stored.genotype.length === 2 ? stored.genotype[1] + stored.genotype[0] : stored.genotype;
    const info = entry.genotypes[stored.genotype] || entry.genotypes[reversed] || entry.genotypes[sortAlleles(stored.genotype)];
    if (!info || info.effect === 'none') continue;
    const cat = entry.category || 'other';
    if (!byCat[cat]) byCat[cat] = [];
    byCat[cat].push({ rsid, gene: stored.gene, variant: stored.variant, genotype: stored.genotype, effect: info.effect, valence: info.valence || 'risk', note: info.note, references: entry.references || [] });
  }

  // Sort categories: those with significant findings first
  const catOrder = Object.entries(byCat).sort(([, a], [, b]) => {
    const aS = a.some(f => f.effect === 'significant') ? 0 : 1;
    const bS = b.some(f => f.effect === 'significant') ? 0 : 1;
    return aS - bS;
  });
  const totalFindings = catOrder.reduce((n, [, fs]) => n + fs.length, 0);

  // Two-axis dot: severity x valence. Protective = green regardless of magnitude;
  // neutral = white circle (lab-artifact / informational, neither bad nor good).
  // Risk severity scales by warmth (significant -> red, moderate -> yellow, mild -> orange).
  const dotFor = (effect, valence) => {
    if (effect === 'none') return '';
    if (valence === 'protective') return '\uD83D\uDFE2';
    if (valence === 'neutral') return '\u26AA';
    if (effect === 'significant') return '\uD83D\uDD34';
    if (effect === 'moderate') return '\uD83D\uDFE1';
    if (effect === 'mild') return '\uD83D\uDFE0';
    return '';
  };
  const catLabels = { methylation: 'Methylation', iron: 'Iron', lipids: 'Lipids', vitaminD: 'Vitamin D', vitaminB12: 'Vitamin B12', bilirubin: 'Bilirubin', thyroid: 'Thyroid', fattyAcids: 'Fatty Acids', bloodSugar: 'Blood Sugar', sexHormones: 'Sex Hormones', alcohol: 'Alcohol', caffeine: 'Caffeine', bodyComposition: 'Body Composition' };

  const metaParts = [];
  if (genetics.source) metaParts.push(escapeHTML(genetics.source));
  if (hasSnps) metaParts.push(`${snpCount} SNPs`);
  if (hasMtdna) metaParts.push(`mtDNA ${escapeHTML(genetics.mtdna.haplogroup)}`);
  if (totalFindings > 0) metaParts.push(`${totalFindings} findings`);
  const latestDate = [genetics.importDate, genetics.mtdna?.importDate].filter(Boolean).sort().pop();
  if (latestDate) metaParts.push(latestDate);

  let html = `<div class="dashboard-section genetics-section" id="genetics-section">
    <div class="section-header" onclick="toggleGeneticsCollapse()" style="cursor:pointer">
      <span>\uD83E\uDDEC Genetics</span>
      <span class="section-meta">${metaParts.join(' \u00B7 ')}
        <span class="genetics-collapse-arrow${collapsed ? ' collapsed' : ''}">\u25BE</span></span>
    </div>`;

  html += `<div class="genetics-body${collapsed ? ' hidden' : ''}">`;

  // mtDNA Haplogroup — prominent display
  if (hasMtdna) {
    const mt = genetics.mtdna;
    const mismatch = detectMtDNAMismatch(genetics);
    html += `<div class="genetics-mtdna">
      <div class="genetics-mtdna-hg"><span class="genetics-mtdna-label">mtDNA Haplogroup:</span> <strong>${escapeHTML(mt.haplogroup)}</strong></div>`;
    if (mt.coupling) {
      html += `<div class="genetics-mtdna-coupling">${escapeHTML(mt.coupling.label)} \u2014 ${escapeHTML(mt.coupling.climate)}</div>`;
    }
    if (mismatch && mismatch.mismatch) {
      html += `<div class="genetics-mtdna-mismatch mismatch-${mismatch.severity}">${escapeHTML(mismatch.message)}</div>`;
    } else if (mismatch && !mismatch.mismatch) {
      html += `<div class="genetics-mtdna-match">${escapeHTML(mismatch.message)}</div>`;
    }
    html += `<div class="genetics-mtdna-refs">Wallace 2015 (<a href="https://pubmed.ncbi.nlm.nih.gov/26406369/" target="_blank" rel="noopener">PMID: 26406369</a>)
      \u00B7 <a href="#" onclick="window.deleteMtDNAData();return false" style="color:var(--text-muted)">remove</a></div>`;
    html += `</div>`;
  }

  if (apoe) {
    html += `<div class="genetics-apoe">APOE: <strong>${escapeHTML(apoe)}</strong></div>`;
  }

  if (totalFindings > 0) {
    // Initially show first 8 findings, rest hidden behind "show all"
    let shown = 0;
    const INITIAL_LIMIT = 8;
    html += `<div class="genetics-findings">`;
    // Legend — explain the dot scheme so users can read severity AND valence at a glance.
    html += `<div class="genetics-legend" title="What the dots mean">
      <span><span class="genetics-legend-dot">🔴</span> significant risk</span>
      <span><span class="genetics-legend-dot">🟡</span> moderate risk</span>
      <span><span class="genetics-legend-dot">🟠</span> mild risk</span>
      <span><span class="genetics-legend-dot">🟢</span> beneficial</span>
      <span><span class="genetics-legend-dot">⚪</span> informational</span>
    </div>`;
    for (const [cat, findings] of catOrder) {
      // Sort significant first within category
      findings.sort((a, b) => (a.effect === 'significant' ? 0 : 1) - (b.effect === 'significant' ? 0 : 1));
      const catLabel = catLabels[cat] || cat;
      const startHidden = shown >= INITIAL_LIMIT;
      html += `<div class="genetics-cat-group${startHidden ? ' genetics-extra' : ''}">`;
      html += `<div class="genetics-cat-label">${escapeHTML(catLabel)}</div>`;
      for (const f of findings) {
        const isExtra = shown >= INITIAL_LIMIT;
        const refLink = f.references.length > 0 && /^https?:/.test(f.references[0]) ? ` <a href="${f.references[0].replace(/"/g, '&quot;')}" target="_blank" rel="noopener" class="detail-genetics-ref" title="Primary study (PubMed)">primary study</a>` : '';
        const snpediaLink = ` <a href="https://www.snpedia.com/index.php/${f.rsid.charAt(0).toUpperCase() + f.rsid.slice(1)}" target="_blank" rel="noopener" class="detail-genetics-ref" title="All studies (SNPedia)">more studies</a>`;
        html += `<div class="genetics-finding-row${isExtra && !startHidden ? ' genetics-extra' : ''}">
          <span>${dotFor(f.effect, f.valence)}</span>
          <span class="genetics-finding-gene">${escapeHTML(f.gene)} ${escapeHTML(f.variant)}</span>
          <span class="genetics-finding-genotype">${escapeHTML(f.genotype)}</span>
          <span class="genetics-finding-note">${escapeHTML(f.note)}${refLink}${snpediaLink}</span>
        </div>`;
        shown++;
      }
      html += `</div>`;
    }
    if (totalFindings > INITIAL_LIMIT) {
      html += `<button class="genetics-show-all" onclick="toggleGeneticsExpand(this)">${totalFindings - INITIAL_LIMIT} more findings</button>`;
    }
    html += `</div>`;
  }

  html += `<div class="genetics-actions">
    <label class="genetics-action-link" onclick="reimportDNA()">Re-import</label>
    <label class="genetics-action-link genetics-action-delete" onclick="window.confirmDeleteDNA()">Delete</label>
  </div>`;
  html += `</div></div>`;

  return html;
}

